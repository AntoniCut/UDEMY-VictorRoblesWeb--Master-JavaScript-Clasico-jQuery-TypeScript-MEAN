'use strict';

console.clear();
console.warn('----------  08-bucle-for  -----  Cargado!!!!!  ----------');
console.log('\n');


(function () {

    //  ******  Bucle FOR  ******

    //  Bucle es una estructura de control que se repite varias veces.

    let numero = 100;

    for (let i = 0; i <= numero; i++) {

        console.log("Vamos por el numero: " + i);

    }

})();
